    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String sql = "SELECT * FROM teachers";
        try {
            ps = con.prepareStatement(sql);
            set = ps.executeQuery();
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            model.setRowCount(0);
            while(set.next()) {
                model.addRow(new String[]{
                    set.getString(1),
                    set.getString(2),
                    set.getString(3),
                    set.getString(4),
                    set.getString(5)
                    
                });
            }
        } catch(Exception ex) {
            JOptionPane.showMessageDialog(this, "Error refreshing table: " + ex.getMessage());
        } finally {
            try {
                if(set != null) set.close();
                if(ps != null) ps.close();
            } catch(Exception e) {
                // Handle cleanup errors silently
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String itemnamer =  jTextFieldid.getText();
        String values = jComboBoxST.getSelectedItem().toString();

        String query = "UPDATE teachers SET status=? WHERE teacherid=?";

        try {
            ps = MyConnection_1.getConnection().prepareStatement(query);

            
            ps.setString(1,values);
            ps.setString(2,itemnamer);

            if(ps.executeUpdate()>0)
            {
                JOptionPane.showMessageDialog(null,"Record Updated Successfully!");

            }
            else
            {
                JOptionPane.showMessageDialog(null,"No Record Found to Update!");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Error: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

