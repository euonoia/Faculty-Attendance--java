    private void jButtonclose2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonclose2ActionPerformed

        String itemnamer = itemnameid1.getText();
        String itemnames = itemname.getText();
        String values = ComboBoxdesc.getSelectedItem().toString();
        String quants = quantity.getText();
        String valuebr = itemnameprice.getText();
        String valueb = ComboBoxstatus.getSelectedItem().toString();

        String query = "UPDATE items SET itemname=?, itemdesc=?, quantity=?,price=?,status=? WHERE referenceid=?";

        try {
            ps = MyConnection.getConnection().prepareStatement(query);

            ps.setString(1,itemnames);  // Changed from itemnamer to itemnames
            ps.setString(2,values);     // Changed order to match query columns
            ps.setString(3,quants);
            ps.setString(4,valuebr);
            ps.setString(5,valueb);
            ps.setString(6,itemnamer);  // referenceid should be itemnamer

            if(ps.executeUpdate()>0)
            {
                JOptionPane.showMessageDialog(null,"Item Updated Successfully!");  // Changed message
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Update Failed!");  // Changed message
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Error: " + ex.getMessage());  // Added error message
        }
    }//GEN-LAST:event_jButtonclose2ActionPerformed

