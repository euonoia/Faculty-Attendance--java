    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
   
                String itemnames = jTextFieldfname.getText().trim();
                String quants = jTextFieldlname.getText().trim();
                String itemnamer = jTextFieldnameuser.getText().trim();
                String itemnamers = jTextFieldlnamepass.getText().trim();

                // Validate input fields
                if(itemnamer.isEmpty() || itemnames.isEmpty() || quants.isEmpty() || itemnamers.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Please fill all fields!");
                    return;
                }

                String query = "INSERT INTO `users`(`firstname`,`lastname`,`username`,`password`)VALUES(?,?,?,?)";

                try {
                    ps = MyConnection_1.getConnection().prepareStatement(query);

                    ps.setString(1, itemnames);
                    ps.setString(2, quants);
                    ps.setString(3, itemnamer);
                    ps.setString(4, itemnamers); // Fixed: using itemnamers instead of undefined 'values'

                    if(ps.executeUpdate() > 0) {
                        JOptionPane.showMessageDialog(null, "New User Added Successfully!");
                       // Assuming you have a method to clear input fields
                    } else {
                        JOptionPane.showMessageDialog(null, "Failed to Add User!");
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Database Error: " + ex.getMessage());
                } finally {
                    try {
                        if(ps != null) ps.close();
                    } catch(SQLException e) {
                        // Handle cleanup errors silently
                    }
                }
    }//GEN-LAST:event_jButton3ActionPerformed

